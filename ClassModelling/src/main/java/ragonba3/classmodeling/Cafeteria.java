class Cafeteria {
    private int numberOfStove;
    private int seatingCapacity;
    private double sqauareFotage;
    private int numberOfServingLines;
    private int mealsPerHours;

    public int getNumberOfStove() {
        return numberOfStove;
    }

    public void setNumberOfStove(int numberOfStove) {
        this.numberOfStove = numberOfStove;
    }

    public int getSeatingCapacity() {
        return seatingCapacity;
    }

    public void setSeatingCapacity(int seatingCapacity) {
        this.seatingCapacity = seatingCapacity;
    }

    public double getSqauareFotage() {
        return sqauareFotage;
    }

    public void setSqauareFotage(double sqauareFotage) {
        this.sqauareFotage = sqauareFotage;
    }

    public int getNumberOfServingLines() {
        return numberOfServingLines;
    }

    public void setNumberOfServingLines(int numberOfServingLines) {
        this.numberOfServingLines = numberOfServingLines;
    }

    public int getMealsPerHours() {
        return mealsPerHours;
    }

    public void setMealsPerHours(int mealsPerHours) {
        this.mealsPerHours = mealsPerHours;
    }
    
    

}
