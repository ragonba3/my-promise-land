package Shape;

/**
 *
 * @author ragonba3
 */
public class Rectangle extends Shape{


   

    @Override
    public String getHeight() {
        return getHeight();
    }

    @Override
    public void setHeight(String height) {
        this.setHeight(height);
    }

    @Override
    public String getWidth() {
        return getWidth();
    }

    @Override
    public void setWidth(String width) {
        this.setWidth(width);
    }

    @Override
    public String getColor() {
        return getColor();
    }

    @Override
    public void setColor(String color) {
        this.setColor(color);
    }

    public String getGetArea() {
        return getGetArea();
    }

    public void setGetArea(String getArea) {
        this.setGetArea(getArea);
    }

    public String getGetPerimeter() {
        return getGetPerimeter();
    }

    public void setGetPerimeter(String getPerimeter) {
        this.setGetPerimeter(getPerimeter);
    }

   


}
