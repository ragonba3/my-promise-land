class walls {
    
    private int numberOfWindows;
    private int wallFacingSouth;
    private int wallFacingNorth;
    private int wallFacingEast;
    private int wallFacingWest;
    private double roofFotage;
    private int numberOfDoors;
    private int numberOfBasementWalls;

    public int getNumberOfWindows() {
        return numberOfWindows;
    }

    public void setNumberOfWindows(int numberOfWindows) {
        this.numberOfWindows = numberOfWindows;
    }

    public int getWallFacingSouth() {
        return wallFacingSouth;
    }

    public void setWallFacingSouth(int wallFacingSouth) {
        this.wallFacingSouth = wallFacingSouth;
    }

    public int getWallFacingNorth() {
        return wallFacingNorth;
    }

    public void setWallFacingNorth(int wallFacingNorth) {
        this.wallFacingNorth = wallFacingNorth;
    }

    public int getWallFacingEast() {
        return wallFacingEast;
    }

    public void setWallFacingEast(int wallFacingEast) {
        this.wallFacingEast = wallFacingEast;
    }

    public int getWallFacingWest() {
        return wallFacingWest;
    }

    public void setWallFacingWest(int wallFacingWest) {
        this.wallFacingWest = wallFacingWest;
    }

    public double getRoofFotage() {
        return roofFotage;
    }

    public void setRoofFotage(double roofFotage) {
        this.roofFotage = roofFotage;
    }

    public int getNumberOfDoors() {
        return numberOfDoors;
    }

    public void setNumberOfDoors(int numberOfDoors) {
        this.numberOfDoors = numberOfDoors;
    }

    public int getNumberOfBasementWalls() {
        return numberOfBasementWalls;
    }

    public void setNumberOfBasementWalls(int numberOfBasementWalls) {
        this.numberOfBasementWalls = numberOfBasementWalls;
    }

    
}
