public class MethodToTheMadness {
    public static void main(String[] args) {
    eatMe(); 
    drinkMe();
    System.out.println("\n - Lewis Carrol, Alice in WOnderland");
        
    }
    
    static void eatMe(){
        System.out.println(" 'But  I don't want to go among mad people,' Alice remarked");
        System.out.println(" 'Oh, you cant help that, ' said the Cat.");
        System.out.print(" 'we are all mad here. I' am mad. You're mad");
        
    }   
    
    static void drinkMe(){
        System.out.println(" 'How do you know I' mad?' said Alice");
        System.out.println(" 'You must be, ' said the Cat, 'or you wouldn't have come here.'");
          
    }
}
